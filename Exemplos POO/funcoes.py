def soma(n1,n2):
    resultado = n1 + n2
    return resultado

def mult(n1,n2):
    resultado = n1 * n2
    return resultado

def sub(n1,n2):
    resultado = n1 - n2
    return resultado

def pot(n1,n2):
    res = n1 ** n2
    return res
